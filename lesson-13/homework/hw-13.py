import re

text = input("Matn kiriting: ")
pattern = r'\b\d{4}-\d{2}-\d{2}\b|\b\d{2}/\d{2}/\d{4}\b'

dates = re.findall(pattern, text)
print("Topilgan sanalar:", dates if dates else "Yo‘q")





